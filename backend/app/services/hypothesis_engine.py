"""Pentest hypothesis engine.

Every tool execution MUST be backed by a hypothesis derived from recon
evidence. This module reads the agent state (findings, tech_stack,
discovered URLs/params/headers, executed tool runs) and produces a list
of testable hypotheses. Each hypothesis is a concrete, falsifiable
statement of the form:

    "ON {target} WHERE {evidence} → TEST {skill} WITH {tool}
     EXPECTING {signal_expected}"

The supervisor consumes the highest-confidence pending hypothesis to
build the next pentest tactic. Without hypotheses → no tactic → no
tool execution. This kills "running tools in the dark".

Each hypothesis is a dict with these fields:

    id                    - stable hash (so we don't re-fire)
    family                - one of: sqli, xss, lfi, ssrf, redirect, crlf,
                                    xxe, rce, ssti, xslt, cors, csv,
                                    auth_bypass, info_disclosure, cms,
                                    weak_crypto
    target                - URL or asset the hypothesis applies to
    target_param          - query/header/body param name (or "")
    rationale             - human-readable WHY: which evidence triggered it
    evidence_signals      - keywords found in evidence that justify the H
    suggested_skill       - SKILL_CATALOG id
    suggested_tool        - kali tool name (must be installed)
    suggested_extra_args  - per-tool argv overrides ({"sqlmap": [...]})
    signal_expected       - what success in the response looks like
    confidence            - 0.0..1.0 score
    learning_id           - reference to the VulnerabilityLearning row
                            that backs the technique (optional)

Hypotheses are generated by rule-based matchers over evidence
(regex-grade rules + tech-stack overlap). LLM-driven hypothesis
generation can plug in later via the same dict schema.
"""
