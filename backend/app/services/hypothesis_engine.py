def hypothesis_for_skill(
    hypotheses: list[dict[str, Any]],
    *,
    skill_id: str | None = None,
    family: str | None = None,
) -> dict[str, Any] | None:
    """Return the highest-confidence hypothesis matching the filters, or None."""
    candidates = []
    for h in hypotheses or []:
        if skill_id and str(h.get("suggested_skill") or "") != skill_id:
            continue
        if family and str(h.get("family") or "") != family:
            continue
        candidates.append(h)
    candidates.sort(key=lambda x: -float(x.get("confidence") or 0))
    return candidates[0] if candidates else None


def hypothesis_to_tactic(h: dict[str, Any], capability: str = "risk_assessment") -> dict[str, Any]:
    """Materialise a hypothesis as a pentest tactic the supervisor can lock to."""
    return {
        "tactic_id": f"hypothesis:{h['family']}:{h['id']}",
        "skill_id": str(h.get("suggested_skill") or "vuln-injection"),
        "capability": capability,
        "objective": h.get("rationale", ""),
        "hypothesis": h.get("rationale", ""),
        "allowed_tools": [str(h.get("suggested_tool") or "")],
        "preferred_tool": str(h.get("suggested_tool") or ""),
        "extra_args": dict(h.get("suggested_extra_args") or {}),
        "strategy_source": "hypothesis_engine",
        "strategy_score": int(round(float(h.get("confidence") or 0) * 100)),
        "learning_techniques": [],
        "evidence_required": [h.get("signal_expected", "")],
        "constraints": [],
        "phase_refs": [],
        "targets": [str(h.get("target") or "")] if h.get("target") else [],
        "reason": (
            f"Hipotese {h.get('family')} sobre {h.get('target')}"
            + (f"?{h.get('target_param')}=" if h.get("target_param") else "")
            + f" — {h.get('rationale','')} (confidence={h.get('confidence',0):.2f})"
        ),
    }
